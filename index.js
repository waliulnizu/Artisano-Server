import dotenv from 'dotenv';

// =========================================================================
// 🚀 UPDATE: ১. সবার আগে সর্বপ্রধান লাইনে .env কনফিগারেশন এক্সিকিউট করা হলো
// =========================================================================
// 🧠 প্রফেশনাল লজিক: নোড জেএস ইম্পোর্ট অবজেক্টগুলো উপর থেকে নিচে সিরিয়ালি রিড করে।
// আমরা যদি এই লাইনের ওপরে 'app.js' ইম্পোর্ট করতাম, তবে app.js এর ভেতরে থাকা Better-Auth ইঞ্জিনটি
// আগে রান হয়ে যেত এবং .env ফাইল থেকে গুগল ক্লায়েন্ট আইডি খুঁজে না পেয়ে টার্মিনালে ওয়ার্নিং ছুড়ে দিত।
// এখন এটি এক নম্বর লাইনে থাকায় পুরো সার্ভার বুট হওয়ার আগেই সব সিক্রেট কি মেমরিতে সিঙ্ক হয়ে যাবে।
dotenv.config({
    path: './.env' 
});

// মেইন এক্সপ্রেস অ্যাপ্লিকেশন ও ডাটাবেস ড্রাইভার ইম্পোর্ট (অবশ্যই ডট-এনভ এর নিচে)
import connectDB from './src/config/db.js'; 
import app from './src/app.js';             

const PORT = process.env.PORT || 5000;

// =========================================================================
// 🔌 ২. ডাটাবেস কানেকশন এবং এক্সপ্রেস সার্ভার লিসেনিং
// =========================================================================
connectDB()
    .then(() => {
        app.listen(PORT, () => {
            console.log(`🚀 Artisano Global Server is running on http://localhost:${PORT}`);
        });
    })
    .catch((err) => {
        console.log("❌ Server connection failed due to Database issue!", err);
    });